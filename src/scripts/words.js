// Function to get a random selection of words from a specific grade
function getRandomWordsByGrade(grade, count = 10) {
    const words = getWordsByGrade(grade);
    
    // Shuffle the array
    const shuffled = [...words].sort(() => 0.5 - Math.random());
    
    // Return the first 'count' elements or all if there are fewer than count
    return shuffled.slice(0, Math.min(count, shuffled.length));
}

// Function to reveal some letters in a word based on grade level and word length
function getPartiallyRevealedWord(word, grade) {
    const letters = word.split('');
    const totalLetters = letters.length;
    
    // Calculate how many letters to hide based on grade level and word length
    let lettersToReveal;
    if (grade === 1) {
        lettersToReveal = Math.ceil(totalLetters / 3); // Reveal 1/3 of letters (easier)
    } else if (grade === 2) {
        lettersToReveal = Math.ceil(totalLetters / 4); // Reveal 1/4 of letters (medium)
    } else {
        lettersToReveal = Math.ceil(totalLetters / 5); // Reveal 1/5 of letters (harder)
    }
    
    // Always reveal at least one letter
    lettersToReveal = Math.max(1, lettersToReveal);
    
    // Choose positions to reveal (prefer first letter for short words)
    const positions = [];
    if (totalLetters <= 4) {
        // For very short words, always reveal the first letter
        positions.push(0);
        lettersToReveal--;
    }
    
    // Randomly select other positions to reveal
    while (positions.length < lettersToReveal) {
        const pos = Math.floor(Math.random() * totalLetters);
        if (!positions.includes(pos)) {
            positions.push(pos);
        }
    }
    
    // Create array with revealed/hidden info
    const result = letters.map((letter, index) => {
        return {
            letter: letter,
            revealed: positions.includes(index)
        };
    });
    
    return result;
}

